package BasicJava;

public class ComparisonOperators {

	public static void main(String[] args) {
		int x=10;
		
		int y=20;
		
		
		   //	==  Equal to    :      x==y            
		    //	! = Not Equal to :  x!=y
			//		> Greater than  : x>y
			//		< Less than :     x<y
			//		>= Greater than or equal to :x>=y
			//		< = Less than or equal to : x<=y
		
		
		System.out.println(x==y);  // false
		System.out.println(x!=y);  //true
		System.out.println(x>y);   //false
		System.out.println(x<y);    //true
		System.out.println(x>=y);  //false
		System.out.println(x<=y);   //true

	}

}
