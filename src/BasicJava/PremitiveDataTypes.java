package BasicJava;

public class PremitiveDataTypes {

	public static void main(String[] args) {
		
		
		// DataType is nothing but which types of information stored in variable 
		// DataType declaration is mandatory for every variable declaration 
	   // All primitive dataTypes are keywords-And Keywords should start with small letters 
		
		int age=20;      // int- DatType  =4byte ; age - Variable ; 20- information 
		
		System.out.println("My age is :"+age);
		
		long mobnum=9975674;   // long -8 byte
		
		System.out.println("my mobile number is "+mobnum);
		
		
		float price=55.26f;
		
		System.out.println("price of product are :"+price);
		
		double bp=120535.123d;
		
		System.out.println("blood pressure of person are :"+bp);
		
		byte b=1;                           // 8 bit
		
		short k=23;                          // 2 byte
		
		boolean a =true;                     // 1 bit 
		
		char character='R';                  // 1byte
		
		

	}

}
